#include"sys.h"


/************************************************************************************
* 函数名称：NVIC_GroupType_Config
* 功能描述：中断的抢占优先级和响应优先级分组函数
* 入口参数：-GroupType：分组方式
* 出口参数：无
*************************************************************************************/
void NVIC_GroupType_Config(u32 GroupType)
{
	SCB->AIRCR = 0x05FA0000 | GroupType;
}
